<!--NOTA: Uso de variables en php y como se declaran

¿Como declaramos una variable?

Las varaibles se declaran usando el signo " $ " (dolar) seguido del nombre de la variable.

Otra nota importante: Al poner la variable $ejemplo (Es un ejemplo solamente) deben tener en cuenta 
que la primera palabra despues del signo " $ " no puede tener espacios ni tildes ni acentos y siempre debe 
ser igual a la primera palabra de la variable que se declare.

Convenciones de nomenclatura:

$name = sigue la convención estándar de PHP (todo en minúsculas)
$Name = usa PascalCase, que no es común en PHP para variables -->